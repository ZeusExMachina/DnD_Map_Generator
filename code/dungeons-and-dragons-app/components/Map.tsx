    private assignEntrances() {
        for (var i: number = this.north[0]; i < this.north[1]; i++) {
            this.room[0][i] = 9
        }
        for (var i = this.south[0]; i < this.south[1]; i++) {
            this.room[9][i] = 9
        }
        for (var i = this.east[0]; i < this.east[1]; i++) {
            this.room[i][9] = 9
        }
        for (var i = this.west[0]; i < this.west[1]; i++) {
            this.room[i][0] = 9
        }
    }
